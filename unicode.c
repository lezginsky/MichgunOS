const unsigned char *get_unicode_font_bitmap(unsigned int codepoint) {
    if (codepoint >= 0x0410 && codepoint <= 0x044F) {
        return cyrillic_font_bitmaps[codepoint - 0x0410];
    }
    return 0;
}

unsigned int utf8_to_codepoint(const unsigned char *str, int *bytes_consumed) {
    if (!str || *str == '\0') {
        *bytes_consumed = 0;
        return 0;
    }

    unsigned char c1 = str[0];
    if (c1 < 0x80) {
        *bytes_consumed = 1;
        return c1;
    } else if ((c1 & 0xE0) == 0xC0) {
        unsigned char c2 = str[1];
        if ((c2 & 0xC0) == 0x80) {
            *bytes_consumed = 2;
            return ((c1 & 0x1F) << 6) | (c2 & 0x3F);
        }
    } else if ((c1 & 0xF0) == 0xE0) {
        unsigned char c2 = str[1];
        unsigned char c3 = str[2];
        if ((c2 & 0xC0) == 0x80 && (c3 & 0xC0) == 0x80) {
            *bytes_consumed = 3;
            return ((c1 & 0x0F) << 12) | ((c2 & 0x3F) << 6) | (c3 & 0x3F);
        }
    } else if ((c1 & 0xF8) == 0xF0) {
        unsigned char c2 = str[1];
        unsigned char c3 = str[2];
        unsigned char c4 = str[3];
        if ((c2 & 0xC0) == 0x80 && (c3 & 0xC0) == 0x80 && (c4 & 0xC0) == 0x80) {
            *bytes_consumed = 4;
            return ((c1 & 0x07) << 18) | ((c2 & 0x3F) << 12) | ((c3 & 0x3F) << 6) | (c4 & 0x3F);
        }
    }

    *bytes_consumed = 1;
    return c1;
}

int utf8_to_codepoints(const char *utf8_str, unsigned int *out_codepoints, int max_len) {
    int count = 0;
    const unsigned char *ptr = (const unsigned char *)utf8_str;
    while (*ptr != '\0' && count < max_len) {
        int consumed = 0;
        unsigned int cp = utf8_to_codepoint(ptr, &consumed);
        if (consumed == 0) break;
        out_codepoints[count++] = cp;
        ptr += consumed;
    }
    return count;
}

